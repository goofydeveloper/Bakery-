class Cookie
# initialize method: name info price 
def initialize(name,info,price)
#  displaying values 
@cookie_name = name 
@cookie_info = info
@cookie_price = price 
# displaying values 
puts "Name is: #@cookie_name"
puts "Info is: #@cookie_info"
